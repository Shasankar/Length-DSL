package com.shasank.funWithLengths

object LengthOperationApp extends App{

  val length1 = 11 inches
  val length2 = 15 inches
  val length3 = 2 feet

  println(length1)
  println(length2)
  println(length3)
  println(length1 + length2)
  println(length3 - length1)
  println(length3 + length1 - length2)

}
